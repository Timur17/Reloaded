void ft_swap(int *a, int *b);

int	main()
{
	int a;
	int b;
	int *pa;
	int *pb;

	a = 1;
	b = 2;
	pa = &a;
	pb = &b;
	ft_swap(pa, pb);
	printf("%d\n%d",a,b); 
	return(0);
}
