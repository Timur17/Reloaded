int ft_iterative_factorial(int nb)

{
	int x;

	if(nb < 0 || nb > 12)
		return(0);
	else
	{	
		x = 1;
		while(nb>0)
		{
			x = x*nb;
			nb--;
		}
	return(x);
	}
}
