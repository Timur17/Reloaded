#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dup;
	int		i;
	int		l;

	l = 0;
	while (src[l] != '\0')
	{
		l++;
	}
	i = 0;
	dup = (char*)malloc(sizeof(*src) * (l + 1));
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
